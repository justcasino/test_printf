#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int j;

	i = 0;
	j = 1;
	while ((*str > 8 && *str < 14) || *str == 32)
		str++;
	if (*str == 45 || *str == 43)
	{
		if (*str == 45)
			j = -1;
		str++;
	}
	while (*str > 47 && *str < 58)
	{
		i = i * 10 + (*str - '0');
		str++;
	}
	return (i * j);
}
